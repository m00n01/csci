#include <iostream>
using namespace std;

int main(){

	cout << "--- 2 to 100 ---" << endl;

	for (int i = 2 ; i <= 100 ; i+=2){

		cout << i << endl;

	}

	cout << endl;

	cout << "--- 10 to 1 ---" << endl;

	for (int i = 10 ; i >= 1 ; i--){

		cout << i << endl;

	}

	cout << "--- Make a tree. ---";

	
	
	return 0;
}